// Коротко про этот файл:
// 1) Подключает все внутренние модули проекта.
// 2) Запускает Tokio runtime.
// 3) Передаёт управление в app::run().
// Подключаем внутренние модули проекта.
// Каждый модуль отвечает за отдельный слой:
// - app: оркестратор всего приложения
// - p2p: создание/настройка libp2p Swarm
// - handlers: обработчики stdin и сетевых событий
// - gateway: HTTP/WS мост для мобильного UI
mod app;
mod api_types;
mod config;
mod gateway;
mod handlers;
mod logging;
mod p2p;
mod sync;

// Стандартный тип ошибки для удобного проброса ошибок вверх.
use std::error::Error;

// Главная точка входа Tokio runtime.
// Здесь мы просто передаём управление в app::run(),
// где лежит вся бизнес-логика приложения.
//
// Почему так:
// - main остаётся максимально тонким;
// - вся сложность вынесена в модули;
// - проще тестировать и читать код по слоям.
#[tokio::main]
async fn main() -> Result<(), Box<dyn Error>> {
    app::run().await
}
